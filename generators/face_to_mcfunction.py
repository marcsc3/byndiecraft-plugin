# Position: face center at ~0, y=50 above town, on the Z=40 plane (facing south)
ORIGIN_X = -24  # center 48-wide grid
ORIGIN_Y = 50   # high in the sky
ORIGIN_Z = 40   # Z plane where the face sits


def generate_mcfunction(grid, origin_x, origin_y, origin_z):
    """Generate mcfunction commands from pixel grid."""
    lines = []
    lines.append("# Cristiano Ronaldo winking face - cloud in the sky")
    lines.append("# Generated by generators/face_to_mcfunction.py")
    lines.append("")

    height = len(grid)

    for row_idx, row in enumerate(grid):
        # Row 0 is top of face = highest Y coordinate
        y = origin_y + (height - 1 - row_idx)

        # Optimize: find runs of same block type
        col = 0
        while col < len(row):
            block_id = row[col]
            if block_id == 0:
                col += 1
                continue

            block = BLOCK_MAP[block_id]

            # Find run length
            run_end = col + 1
            while run_end < len(row) and row[run_end] == block_id:
                run_end += 1

            run_length = run_end - col
            x1 = origin_x + col
            x2 = origin_x + run_end - 1

            if run_length == 1:
                lines.append(f"setblock ~{x1} ~{y} ~{origin_z} {block}")
            else:
                lines.append(f"fill ~{x1} ~{y} ~{origin_z} ~{x2} ~{y} ~{origin_z} {block}")

            col = run_end

    return "\n".join(lines) + "\n"


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    repo_root = os.path.dirname(script_dir)
    output_path = os.path.join(
        repo_root, "data-pack", "data", "byndiecraft", "function", "ronaldo_cloud.mcfunction"
    )

    content = generate_mcfunction(FACE_GRID, ORIGIN_X, ORIGIN_Y, ORIGIN_Z)

    with open(output_path, "w") as f:
        f.write(content)

    print(f"Generated: {output_path}")
    print(f"Total lines: {len(content.splitlines())}")


if __name__ == "__main__":
    main()
